# for getting input from the user use the input command  input()
# whatever value we take from the user should be stored in a variable
# let us ask the user for his/her name and age and lets print it


# the variable name stores the input from the user
name=input('Enter your name : ')

# the variable age stores the age of the  user
# since age is a number , we convert the input from the user into int type 
age=int(input('Enter your age : '))

# printing the output
print(f" My name  is {name} and I'm {age} years old . ")

